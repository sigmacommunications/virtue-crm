                'user_id' => \Auth::id(),
            ]);

            return redirect()->to('/leaves-type')->with(['success'=>'Leave type saved']);
        }
        catch(\Exception $e)
        {
            return redirect()->back()->with(['error'=>$e->getMessage()]);
        }
    }

